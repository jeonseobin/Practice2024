#include <stdio.h>

int main()
{
    int x = 1, y = 2;

    printf("x > y : %d\n", x > y);
    printf("x < y : %d\n", x < y);
    printf("x >= y : %d\n", x >= y);
    printf("x <= y : %d\n", x <= y);
    printf("x == y : %d\n", x == y);
    printf("x != y : %d\n", x != y);

    return 0;
}
